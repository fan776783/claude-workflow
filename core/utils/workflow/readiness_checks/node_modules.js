#!/usr/bin/env node
/**
 * @file node_modules.js — readiness check：项目根存在 node_modules 目录。
 *
 * 单文件单 named export `{ name, check }`。由 readiness_checks/index.js 自动扫描注册。
 * CommonJS。
 */

const fs = require('fs')
const path = require('path')

/**
 * 检查 projectRoot 下是否存在 node_modules 目录。
 *
 * @param {string} projectRoot  项目根绝对路径
 * @returns {{ok:boolean, reason?:string, suggested_fix?:string}}
 */
function check(projectRoot) {
  if (fs.existsSync(path.join(projectRoot, 'node_modules'))) {
    return { ok: true }
  }
  return {
    ok: false,
    reason: 'node_modules 不存在',
    suggested_fix: 'pnpm install (or npm install)',
  }
}

module.exports = { name: 'node_modules', check }
